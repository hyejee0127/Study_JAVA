package while_;

public class Test1 {

	public static void main(String[] args) {
		// 구구단 출력
		// < 2단 >
		// 2 * 1 = 2
		// 2 * 2 = 4
		// 2 * 3 = 6
		// ...
		// 2 * 9 = 18
		int dan = 127;
		int i = 1;
		System.out.println("< " + dan + "단 >");
		while(i <= 9) {
			System.out.println(dan + " * " + i + " = " + (dan * i));
			i++;
		}
		
		
		
		
		
		
		
		
	}

}
